// desktrukturing element = mengubah struktur element dengan cara yang lebih elegan

// contoh :
// desctructuring array
let items = ["Asus", "laptop"];
let [nama, type] = items;

console.log(nama);
console.log(type);

let students = ["Joko", 3.55, true]; //data dalam array belom tahu isi apa , maka perlu diubah menggunakan desctructuring

let [name, gpa, isGraduate] = students;

console.log(name);
console.log(gpa);
console.log(isGraduate);

